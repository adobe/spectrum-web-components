import { spawnSync } from 'node:child_process';
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import postcss from 'postcss';

const LAYER = 'swc-global-elements';
const FENCE_OPEN_RE = /^@global-exclude(?:\s*:.*)?$/;
const FENCE_CLOSE = '@global-exclude-end';

// Attributes in the stable SWC API whose modifier values are prefixed with the
// first hyphen-segment of the attribute name to avoid ambiguous short tokens:
//   size="l"           → sizeL   (bare "l" conveys nothing in a global stylesheet)
//   static-color="white" → staticWhite  ("static" is the semantic distinguisher)
const SWC_PREFIXED_ATTRS = ['size', 'static-color'];

